#Dicionarios
#Sintaxe do Dicionario {key: value}
notas = {
    'Gabriel': 10,
    'João': 8,
    'Maria': 9,
    'Pedro': 7
}
print("A nota de gabriel e:",notas['Gabriel']) #Acessando o valor do dicionario

#diferente dos outras estruturas vc acessa o valor pela key

