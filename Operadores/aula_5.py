#Operadores relacionais 

resultado = 2 == 2 # o operador relacional de == pergunta se algo seria igual a outra coisa(no caso o == esta perguntado se 2 e igual a 2 )
print('2 e igual a 2 ?', resultado)

resultado = 2 != 2 # o operador relacional de != pergunta se algo e diferente de outra coisa ( no nosso caso ele estaria perguntando se 2 e diferente de 2 )
print('2 e diferente de 2?', resultado)
resultado = 2 > 2
print('2 e maior que 2?', resultado)
resultado = 2 >= 2
print('2 e maior ou igual a 2?', resultado)
resultado = 2 < 2 
print('2 e menor que 2?', resultado)
resultado = 2 <= 2
print('2 e menor ou igual a 2?', resultado)




